async function main() {
  // Create admin user with password from env or generate secure random
  const adminPasswordPlain = process.env.ADMIN_SEED_PASSWORD || generateSecurePassword()
  const isGenerated = !process.env.ADMIN_SEED_PASSWORD
  const adminPassword = await bcrypt.hash(adminPasswordPlain, 10)

  const admin = await prisma.user.upsert({
    where: { email: 'admin@perfumes.store' },
    update: {},
    create: {
      email: 'admin@perfumes.store',
      passwordHash: adminPassword,
      name: 'Admin',
      role: Role.ADMIN,
    },
  })

  console.log('Created admin user:', admin.email)
  if (isGenerated) {
    console.log('⚠️  Generated admin password:', adminPasswordPlain)
    console.log('   Save this password! It will not be shown again.')
  }

  // Collect all unique fragrance families from perfume data
  const fragranceFamilySet = new Set<string>()
  perfumes.forEach(p => fragranceFamilySet.add(p.fragranceFamily))
  const fragranceFamilies = Array.from(fragranceFamilySet)

  // Collect all unique longevities
  const longevitySet = new Set<string>()
  perfumes.forEach(p => longevitySet.add(p.longevity))
  const longevities = [
    { name: 'Weak', sortOrder: 1 },
    { name: 'Moderate', sortOrder: 2 },
    { name: 'Long-lasting', sortOrder: 3 },
    { name: 'Very long-lasting', sortOrder: 4 },
  ]

  // Collect all unique sillages
  const sillages = [
    { name: 'Intimate', sortOrder: 1 },
    { name: 'Moderate', sortOrder: 2 },
    { name: 'Strong', sortOrder: 3 },
    { name: 'Enormous', sortOrder: 4 },
  ]

  const seasons = ['Spring', 'Summer', 'Fall', 'Winter']

  // Collect unique occasions from perfume data
  const occasionSet = new Set<string>()
  perfumes.forEach(p => p.occasion.forEach(o => occasionSet.add(o)))
  const occasions = Array.from(occasionSet)

  // Upsert fragrance families
  for (const name of fragranceFamilies) {
    await prisma.fragranceFamily.upsert({
      where: { name },
      update: {},
      create: { name },
    })
  }
  console.log('Seeded fragrance families:', fragranceFamilies.length)

  // Upsert longevities
  for (const { name, sortOrder } of longevities) {
    await prisma.longevity.upsert({
      where: { name },
      update: { sortOrder },
      create: { name, sortOrder },
    })
  }
  console.log('Seeded longevities')

  // Upsert sillages
  for (const { name, sortOrder } of sillages) {
    await prisma.sillage.upsert({
      where: { name },
      update: { sortOrder },
      create: { name, sortOrder },
    })
  }
  console.log('Seeded sillages')

  // Upsert seasons
  for (const name of seasons) {
    await prisma.season.upsert({
      where: { name },
      update: {},
      create: { name },
    })
  }
  console.log('Seeded seasons')

  // Upsert occasions
  for (const name of occasions) {
    await prisma.occasion.upsert({
      where: { name },
      update: {},
      create: { name },
    })
  }
  console.log('Seeded occasions:', occasions)

  // Fetch all lookup data
  const allFragranceFamilies = await prisma.fragranceFamily.findMany()
  const allLongevities = await prisma.longevity.findMany()
  const allSillages = await prisma.sillage.findMany()
  const allSeasons = await prisma.season.findMany()
  const allOccasions = await prisma.occasion.findMany()

  // Create maps for quick lookup
  const fragranceFamilyMap = new Map(allFragranceFamilies.map(f => [f.name, f.id]))
  const longevityMap = new Map(allLongevities.map(l => [l.name, l.id]))
  const sillageMap = new Map(allSillages.map(s => [s.name, s.id]))
  const seasonMap = new Map(allSeasons.map(s => [s.name, s.id]))
  const occasionMap = new Map(allOccasions.map(o => [o.name, o.id]))

  // Delete existing products before seeding new ones
  await prisma.product.deleteMany({})
  console.log('Cleared existing products')

  // Create products
  for (const perfume of perfumes) {
    const fragranceFamilyId = fragranceFamilyMap.get(perfume.fragranceFamily)
    const longevityId = longevityMap.get(perfume.longevity)
    const sillageId = sillageMap.get(perfume.sillage)

    if (!fragranceFamilyId || !longevityId || !sillageId) {
      console.error(`Missing lookup data for ${perfume.name}:`, {
        fragranceFamilyId,
        longevityId,
        sillageId,
      })
      continue
    }

    // Handle "All" season by connecting to all seasons
    const seasonIds = perfume.season.includes('All')
      ? allSeasons.map(s => ({ id: s.id }))
      : perfume.season
          .map(s => seasonMap.get(s))
          .filter((id): id is number => id !== undefined)
          .map(id => ({ id }))

    const occasionIds = perfume.occasion
      .map(o => occasionMap.get(o))
      .filter((id): id is number => id !== undefined)
      .map(id => ({ id }))

    // Generate random stock between 50 and 200
    const stock = Math.floor(Math.random() * 151) + 50

    // Get image filename
    const imageFilename = imageFilenames[perfume.id]
    const imageUrl = imageFilename ? `/uploads/products/${imageFilename}` : null

    // Generate SEO-friendly slug
    const slug = generateSlug(perfume.brand, perfume.name, perfume.concentration)

    await prisma.product.create({
      data: {
        slug,
        name: perfume.name,
        brand: perfume.brand,
        concentration: mapConcentration(perfume.concentration),
        gender: mapGender(perfume.gender),
        fragranceFamilyId,
        topNotes: perfume.topNotes,
        heartNotes: perfume.heartNotes,
        baseNotes: perfume.baseNotes,
        volumeMl: perfume.volumeMl,
        priceRON: perfume.priceUSD, // Using USD as RON for now
        launchYear: perfume.launchYear,
        perfumer: perfume.perfumer,
        longevityId,
        sillageId,
        seasons: { connect: seasonIds },
        occasions: { connect: occasionIds },
        rating: perfume.rating,
        stock,
        imageUrl,
        description: perfume.description,
      },
    })
    console.log(`Created product: ${perfume.name}`)
  }

  console.log(`Seeding complete. Created ${perfumes.length} products.`)
}

main()
  .catch((e) => {
    console.error(e)
    process.exit(1)
  })
  .finally(async () => {
    await prisma.$disconnect()
  })
