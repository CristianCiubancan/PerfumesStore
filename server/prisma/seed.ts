// Inline generateSlug to make seed self-contained for production builds
function generateSlug(brand: string, name: string, concentration: string): string {
  const concentrationShort = concentration
    .replace(/_/g, ' ')
    .replace('Extrait de Parfum', 'Extrait')

  const combined = `${brand} ${name} ${concentrationShort}`

  return combined
    .toLowerCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/&/g, 'and')
    .replace(/['"']/g, '')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
    .replace(/-+/g, '-')
}

function generateSecurePassword(): string {
  return crypto.randomBytes(16).toString('base64url')
}

// Map JSON concentration to Prisma enum
function mapConcentration(concentration: string): Concentration {
  const mapping: Record<string, Concentration> = {
    'Eau de Parfum': Concentration.Eau_de_Parfum,
    'Eau de Toilette': Concentration.Eau_de_Toilette,
    'Eau de Toilette Intense': Concentration.Eau_de_Toilette,
    'Cologne': Concentration.Eau_de_Cologne,
    'Eau de Cologne': Concentration.Eau_de_Cologne,
    'Parfum': Concentration.Parfum,
    'Extrait de Parfum': Concentration.Extrait_de_Parfum,
  }
  return mapping[concentration] || Concentration.Eau_de_Parfum
}

// Map JSON gender to Prisma enum
function mapGender(gender: string): Gender {
  const mapping: Record<string, Gender> = {
    'Men': Gender.Men,
    'Women': Gender.Women,
    'Unisex': Gender.Unisex,
  }
  return mapping[gender] || Gender.Unisex
}

